CREATE TABLE STAFF (
	STAFF_ID INTEGER primary key generated always as identity,
	STAFF_USERNAME VARCHAR(30) NOT NULL UNIQUE,
	STAFF_PASSWORD VARCHAR(30) NOT NULL,
	STAFF_EMAIL VARCHAR(255),
	STAFF_FULLNAME VARCHAR(255),
	STAFF_IC_NO VARCHAR(255),
	STAFF_PH_NO VARCHAR(255),
	STAFF_ADDRESS VARCHAR(255),
	STAFF_GENDER VARCHAR(10)
);

CREATE TABLE PRODUCT_CATEGORY(
    PRODUCT_CAT_ID INTEGER primary key generated always as identity,
    PRODUCT_CAT_NAME VARCHAR(255),
	PRODUCT_CAT_IMAGE VARCHAR(199)
);

CREATE TABLE PRODUCT (
    PRODUCT_ID INTEGER primary key generated always as identity,
	PRODUCT_NAME VARCHAR(255),
	PRODUCT_PRICE DOUBLE,
	PRODUCT_QTY INT,
	PRODUCT_DESC VARCHAR(255),
	PRODUCT_IMAGE VARCHAR(199),
    FK_PROD_CAT_ID INTEGER,
    foreign key (FK_PROD_CAT_ID) references PRODUCT_CATEGORY(PRODUCT_CAT_ID)
);

CREATE TABLE CUSTOMER (
	CUSTOMER_ID INTEGER primary key generated always as identity,
	CUSTOMER_USERNAME VARCHAR(30) NOT NULL UNIQUE,
	CUSTOMER_PASSWORD VARCHAR(30) NOT NULL,
	CUSTOMER_EMAIL VARCHAR(255),
	CUSTOMER_FULLNAME VARCHAR(255),
	CUSTOMER_IC_NO VARCHAR(255),
	CUSTOMER_PH_NO VARCHAR(255),
	CUSTOMER_ADDRESS VARCHAR(255),
	CUSTOMER_GENDER VARCHAR(10)
);

CREATE TABLE ORDER_TABLE(
	ORDER_ID INTEGER primary key generated always as identity,
	ORDER_UUID VARCHAR(255),
	ORDER_RECEIVE_NAME VARCHAR(255),
	ORDER_ADDRESS VARCHAR(255),
	ORDER_PAYMENT_AMOUNT DOUBLE,
	ORDER_STATUS VARCHAR(25),
	ORDER_QTY INTEGER,
    ORDER_DATE DATE,
	CK_PRODUCT_ID INTEGER,
	CK_CUSTOMER_ID INTEGER,
	foreign key (CK_PRODUCT_ID) references PRODUCT(PRODUCT_ID),
	foreign key (CK_CUSTOMER_ID) references CUSTOMER(CUSTOMER_ID)
);

CREATE TABLE PAYMENT(
	PAYMENT_ID INTEGER primary key generated always as identity,
	PAYMENT_NAME VARCHAR(255),
	CERDIT_CARD_NUMBER VARCHAR(255),
	EXP_MONTH VARCHAR(255),
	CCV VARCHAR(10),
	EXP_YEAR VARCHAR(10),
	PAYMENT_DATE DATE,
	FK_ORDER_ID INTEGER,
	foreign key (FK_ORDER_ID) references ORDER_TABLE(ORDER_ID)
);

CREATE TABLE FEEDBACK(
	FEEDBACK_ID INTEGER primary key generated always as identity,
	COMMENT_FEEDBACK VARCHAR(255),
	STAFF_COMMENT VARCHAR(255),
	FEEDBACK_DATE DATE,
	RATING INTEGER,
	FK_CUSTOMER_ID INTEGER,
	FK_PRODUCT_ID INTEGER,
	foreign key (FK_PRODUCT_ID) references PRODUCT(PRODUCT_ID),
	foreign key (FK_CUSTOMER_ID) references CUSTOMER(CUSTOMER_ID)
);

